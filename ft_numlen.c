int	ft_numlen(int n, unsigned int m)
{
	int	k;

	k = 0;
	if (n == '\0' && m == '\0')
		return (1);
	if (n < 0)
		n *= -1;
	if ((unsigned int)n > m)
	{
		while (n != 0)
		{
			k++;
			n = n / 10;
		}
	}
	else
	{
		while (m != 0)
		{
			k++;
			m = m / 10;
		}
	}
	return (k);
}
